class Solution{
public:
    int maxIndexDiff(int arr[], int n) {
        // code here
        if(n==0||n==1) return 0;
        int i=0,j=n-1,ans=INT_MIN;
        while(j>i){
            if(arr[i]>arr[j]) j--;
            else{
                ans=max(ans,j-i);
                i++;
                j=n-1;
            }
        }
        return ans;
    }

};


